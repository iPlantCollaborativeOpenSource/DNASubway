my %newdata;
for my $input (sort keys %seq) {
     my $fh = IO::String->new($seq{$input});
     my $seq = Bio::SeqIO->new(
                              -fh => $fh)->next_seq;
     $newdata{$input} = $seq->seq;
}
is($newdata{raw}, $newdata{fasta}, 'format');

